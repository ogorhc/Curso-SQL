
SELECT
	COUNT(*) AS TOTAL_USERS,
	MIN(FOLLOWERS) AS MIN_FOLLOWERS,
	MAX(FOLLOWERS) AS MAX_FOLLOWERS,
	ROUND(AVG(FOLLOWERS), 1) AS AVG_FOLLOWERS,
	SUM(FOLLOWERS) / COUNT(ID) AS AVG_MANUAL
FROM
	USERS;

SELECT
	FIRST_NAME,
	LAST_NAME,
	FOLLOWERS
FROM
	USERS
WHERE
	FOLLOWERS = 4
	OR FOLLOWERS = 4999
ORDER BY
	FOLLOWERS;

SELECT
	COUNT(*),
	FOLLOWERS
FROM
	USERS
WHERE
	FOLLOWERS = 4
	OR FOLLOWERS = 4999
GROUP BY
	FOLLOWERS
ORDER BY
	FOLLOWERS;